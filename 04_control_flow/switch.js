// switch in JS (Control Flow)

const month = "sept"

switch (month) {

    case "Jan":
        console.log("January")
        break;

    case "Feb":
        console.log("February")
        break;

    case "mar":
        console.log("march")
        break;

    case "sept":
        console.log("September")
        break;

    default:
        console.log("Default case")
        break;
}